package second_Lab_book_Java_programs;

public class To_Check_Vowel_Or_Not {

	public static void main(String[] args) {
		char ch = 'i';
		if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
		System.out.println(ch + " is vowel");
		else
		System.out.println(ch + " is consonant");	}

}
